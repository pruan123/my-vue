<template>
  <div>
    <p>input1=><input type="text" id="input1"></p>
    <p>input2=><input type="text" id="input2"></p>
    <div>
      我每次比input1的值增加1=>
      <span id="span"></span>
    </div>
  </div>
</template>

<script>
  export default {
    data() {
      return {
        data: "hello world",
        args: [0, 1, 2],
        obj1: {},
        obj2: {}
      }
    },
    methods: {
      test(a, b, c) {
        console.log(a, b, c)
      }
    },
    mounted() {
      this.test.apply(null, this.args)
      this.test(...this.args) //展开运算符
      Object.defineProperties(this.obj1, {name: {value: 'aaa'}}) 
      Object.defineProperties(this.obj2, {detail: {value: 'bbb'}})
      var obj3 = Object.assign(this.obj1, this.obj2)
      console.log(this.obj2, 88)
      console.log(obj3, 99)
    }
  }
  var oInput1 = document.getElementById('input1');
  var oInput2 = document.getElementById('input2');
  var oSpan = document.getElementById('span');
  var obj = {};
  Object.defineProperties(obj, {
    val1: {
      configurable: true,
      get: function () {
        oInput1.value = 0;
        oInput2.value = 0;
        oSpan.innerHTML = 0;
        return 0
      },
      set: function (newValue) {
        oInput2.value = newValue;
        oSpan.innerHTML = Number(newValue) ? Number(newValue) : 0
      }
    },
    val2: {
      configurable: true,
      get: function () {
        oInput1.value = 0;
        oInput2.value = 0;
        oSpan.innerHTML = 0;
        return 0
      },
      set: function (newValue) {
        oInput1.value = newValue;
        oSpan.innerHTML = Number(newValue) + 1;
      }
    }
  })
  oInput1.value = obj.val1;
  oInput1.addEventListener('keyup', function () {
    obj.val1 = oInput1.value;
  }, false)
  oInput2.addEventListener('keyup', function () {
    obj.val2 = oInput2.value;
  }, false)
</script>

